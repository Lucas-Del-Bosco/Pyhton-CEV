print('Escreva um programa que leia um valor em metros e o exiba convertido em centímetros e milímetros:')
m = float(input('Escreva uma distância em metros: '))
cm = m * 100
mm = m * 1000

print(f'De acordo com a distância colocada, o valor em centímetros é {cm}cm e o valor em milímetros é {mm}mm.')
